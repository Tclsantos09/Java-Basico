package ex04_2_exercise;

public class TiposVariaveis {

	public static void main(String[] args) {
		byte _byte = 127;
		short _short = 32767;
		int _int = 5_547_452;
		float _float = 78.56f;
		double _double = 45.98;
		char _char = (char) 83;
		boolean _boolean = false;

		System.out.println("Byte: " + _byte + "\nShort: " + _short + "\nInt: "
				+ _int + "\nFloat: " + _int + "\nDouble: " + _double + "\nChar: "
				+ _char + "\nBoolean: " + _boolean);
		
	}

}
